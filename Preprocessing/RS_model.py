import numpy as np
from sklearn.linear_model import LinearRegression
from sklearn.metrics import r2_score

# =========================================
# 1. 表4-1数据
# =========================================

cities = ["北京", "广州", "武汉", "郑州", "西安", "杭州"]

S = np.array(
    [
        250000,
        1000000,
        250000,
        1000000,
        250000,
        1000000,
        250000,
        1000000,
        250000,
        1000000,
        250000,
        1000000,
    ],
    dtype=float,
)

R = np.array(
    [
        0.2149,
        0.0712,
        0.2302,
        0.0788,
        0.3378,
        0.1014,
        0.3380,
        0.1274,
        0.4209,
        0.1354,
        0.3349,
        0.1038,
    ],
    dtype=float,
)

# =========================================
# 2. 对数变换
# =========================================

x = np.log(S).reshape(-1, 1)
y = np.log(R)

# =========================================
# 3. 最小二乘拟合
# =========================================

model = LinearRegression()
model.fit(x, y)

slope = model.coef_[0]  # slope = -b
intercept = model.intercept_  # ln(a)

b = -slope
a = np.exp(intercept)

# =========================================
# 4. 拟合优度
# =========================================

y_pred_log = model.predict(x)
r2_log = r2_score(y, y_pred_log)

R_pred = a * (S ** (-b))
r2 = r2_score(R, R_pred)

# =========================================
# 5. 输出结果
# =========================================

print("========== 幂函数拟合结果 ==========")
print(f"a = {a:.6e}")  # 科学计数法
print(f"b = {b:.6f}")
print(f"R² (log空间) = {r2_log:.6f}")
print(f"R² (原始空间) = {r2:.6f}")

# =========================================
# 6. 输出标准模型表达式
# =========================================

print("\n========== 拟合模型 ==========")
print(f"R = {a:.3e} * S^(-{b:.3f})")
